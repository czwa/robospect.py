import sys
import robospect as RS

def main(args=None):
    if args is None:
        args = sys.argv
    config  = RS.Config(args)
    spectra = config.read_spectrum()
    results = spectra.fit()
    config.write_results(spectra)

if __name__ == "__main__":
    main()


